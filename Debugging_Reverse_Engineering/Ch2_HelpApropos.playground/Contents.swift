//The "help" command
//(lldb) help 로 lldb 도움말을 볼 수 있다.
//(lldb) help breakpoint 등으로 해당 항목의 세부 도움말을 볼 수 있다.
//(lldb) help breakpoint name 등으로 세부 도움말을 볼 수 있다.




//The "apropos" command
//apropos는 검색엔진을 사용해 웹에서 키워드를 찾는 것과 비슷한 명령어로, 명령어의 이름을 정확히 모르지만 특정 단어나 구를 검색한다.
//apropos는 LLDB 문서에서 단어나 문자열 대소문자를 구분하지 않고 검색해서 일치하는 결과를 반환한다.
//(lldb) apropos swift 로 swift라는 단어와 관련된 모든 결과를 보여준다.
//(lldb) apropos "reference count" 로 특정 문장을 검색할 수도 있다. reference count에 도움되는 자료를 검색한다.
//apropos는 검색에 대해 하나의 파라미터만을 허용하므로, 입력을 단일 인수로 처리하려면 따옴표가 필요하다.
